#             |---------------------|
# <module>    |                     |
#             |---------------------|
#
#             |---------------------|
# print_n     | s--->'Hello' n--->2 |             |
#             |---------------------|
#
#             |---------------------|
# print_n     | s--->'Hello' n--->1 |              |
#             |---------------------|

def print_n(s, n):
    if n <= 0:
        return
    print(s)
    print_n(s, n-1)

print_n('Hello', 2)
